import turtle

turtle.shape("turtle")
print(turtle.pos())

# L
turtle.up()
turtle.goto(-200, 100)
turtle.down()
turtle.goto(-200, -100)
turtle.goto(-75, -100)

# space
turtle.up()
turtle.goto(-25, -100)
turtle.down()

# N
turtle.goto(-25, 100)
